CREATE VIEW EMBCPROD.V_TIP_INTEL_TSA_REPORT (
   UNIQUE_KEY, 
   HISTORY_VERSION, 
   DELETED, 
   FOREIGN_DOC, 
   WEB_PAGE_LINK, 
   OWNED_BY, 
   COL_TIPINTELPARENTID, 
   COL_TIPTSANAMEID, 
   COL_TIPTSACATEGORY, 
   COL_TIPTSADATE, 
   COL_TIPTSAPS, 
   COL_TIPTSACLASS, 
   COL_TIPTSAINFSECTOR, 
   COL_TIPTSALASTNAME, 
   COL_TIPTSAMIDNAME, 
   COL_TIPTSAFIRSTNAME, 
   COL_TIPTSAJOBTITLE, 
   COL_TIPTSAEMAIL, 
   COL_TIPTSAHOME, 
   COL_TIPTSAWORK, 
   COL_TIPTSACELL, 
   COL_TIPTSAFAX, 
   COL_TIPTSAOTHER, 
   COL_TIPTSAWEBSITE, 
   COL_TIPTSAADDITIONALINFO, 
   COL_TIPTSAADDRESS, 
   COL_TIPTSAPOBOX, 
   COL_TIPTSACITY, 
   COL_TIPTSASTATE, 
   COL_TIPTSAZIP, 
   COL_TIPTSACOUNTRY, 
   SITE_NAME, 
   SITE_TYPE, 
   STREET_ADDRESS, 
   APT_LOT_NO, 
   CITY, 
   COUNTRY_NAME, 
   COUNTY_NAME, 
   GEO_AREA, 
   STATE_NAME, 
   ZIP_CODE, 
   INTERSECTION_STREET1, 
   INTERSECTION_STREET2, 
   ADDITIONAL_INFO, 
   SHOW_ON_MAP, 
   LATITUDE, 
   LONGITUDE, 
   F_LATITUDE, 
   F_LONGITUDE, 
   GEO_LOCATED_BY, 
   CREATION_DATE, 
   CREATED_BY, 
   MODIFICATION_DATE, 
   MODIFIED_BY, 
   TIMEZONE)
AS 
   SELECT 
      TSA.TIP_INTEL_TSA_ID AS UNIQUE_KEY, 
      CASE REPT.STATUS
         WHEN 'O' THEN 'Yes'
         ELSE 'No'
      END AS HISTORY_VERSION, 
      CASE REPT.STATUS
         WHEN 'D' THEN 'Yes'
         ELSE 'No'
      END AS DELETED, 
      CASE REPT.CREATED_BY
         WHEN NULL THEN 'Yes'
         ELSE 'No'
      END AS FOREIGN_DOC, 
      REPT.WEB_PAGE_LINK, 
      CASE REPT.IS_OWNER
         WHEN 'Y' THEN 'Local'
         ELSE 'No'
      END AS OWNED_BY, 
      TSA.COL_TIPINTELPARENTID, 
      TSA.COL_TIPTSANAMEID, 
      TSA.COL_TIPTSACATEGORY, 
      TSA.COL_TIPTSADATE, 
      TSA.COL_TIPTSAPS, 
      TSA.COL_TIPTSACLASS, 
      TSA.COL_TIPTSAINFSECTOR, 
      TSA.COL_TIPTSALASTNAME, 
      TSA.COL_TIPTSAMIDNAME, 
      TSA.COL_TIPTSAFIRSTNAME, 
      TSA.COL_TIPTSAJOBTITLE, 
      TSA.COL_TIPTSAEMAIL, 
      TSA.COL_TIPTSAHOME, 
      TSA.COL_TIPTSAWORK, 
      TSA.COL_TIPTSACELL, 
      TSA.COL_TIPTSAFAX, 
      TSA.COL_TIPTSAOTHER, 
      TSA.COL_TIPTSAWEBSITE, 
      TSA.COL_TIPTSAADDITIONALINFO, 
      TSA.COL_TIPTSAADDRESS, 
      TSA.COL_TIPTSAPOBOX, 
      TSA.COL_TIPTSACITY, 
      TSA.COL_TIPTSASTATE, 
      TSA.COL_TIPTSAZIP, 
      TSA.COL_TIPTSACOUNTRY, 
      L.SITE_NAME, 
      L.SITE_TYPE, 
      L.STREET_ADDRESS, 
      L.APT_LOT_NO, 
      L.CITY, 
      L.COUNTRY_NAME, 
      L.COUNTY_NAME, 
      L.GEO_AREA, 
      L.STATE_NAME, 
      L.ZIP_CODE, 
      L.INTERSECTION_STREET1, 
      L.INTERSECTION_STREET2, 
      L.ADDITIONAL_INFO, 
      G.SHOW_ON_MAP, 
      G.LATITUDE, 
      G.LONGITUDE, 
      CAST(G.LATITUDE AS float(53)) AS F_LATITUDE, 
      CAST(G.LONGITUDE AS float(53)) AS F_LONGITUDE, 
      G.GEO_LOCATED_BY, 
      REPT.CREATION_DATE, 
      CASE 
         WHEN UC.USER_NAME IS NULL THEN 
            (
               SELECT TBL_REMOTE_DOCUMENT_USER_INFO.CREATED_BY_USER_LOGIN_ID
               FROM EMBCPROD.TBL_REMOTE_DOCUMENT_USER_INFO
               WHERE TBL_REMOTE_DOCUMENT_USER_INFO.GLOBAL_REPORT_ID = TSA.TIP_INTEL_TSA_ID
            )
         ELSE UC.USER_NAME
      END AS CREATED_BY, 
      REPT.MODIFICATION_DATE, 
      CASE 
         WHEN UM.USER_NAME IS NULL THEN 
            (
               SELECT TBL_REMOTE_DOCUMENT_USER_INFO$2.MODIFIED_BY_USER_LOGIN_ID
               FROM EMBCPROD.TBL_REMOTE_DOCUMENT_USER_INFO  AS TBL_REMOTE_DOCUMENT_USER_INFO$2
               WHERE TBL_REMOTE_DOCUMENT_USER_INFO$2.GLOBAL_REPORT_ID = TSA.TIP_INTEL_TSA_ID
            )
         ELSE UM.USER_NAME
      END AS MODIFIED_BY, 
      
         (
            SELECT TBL_EEMS_CONFIGURATION.CUSTOMER_TIMEZONE
            FROM EMBCPROD.TBL_EEMS_CONFIGURATION, EMBCPROD.TBL_REPORT
            WHERE TBL_EEMS_CONFIGURATION.CONFIGURATION_ID = TBL_REPORT.GLOBAL_REPORT_ID AND TBL_REPORT.STATUS = 'A'
         ) AS TIMEZONE
   FROM 
      EMBCPROD.TBL_TIP_INTEL_TSA_REPORT  AS TSA, 
      ((EMBCPROD.TBL_REPORT  AS REPT 
         LEFT JOIN EMBCPROD.TBL_LOCATION  AS L 
         ON REPT.LOCATION_ID = L.LOCATION_ID) 
         LEFT JOIN EMBCPROD.TBL_GEO_LOCATION_MAPPING  AS G 
         ON REPT.GEO_LOCATION_MAPPING_ID = G.GEO_LOCATION_MAPPING_ID) 
         LEFT JOIN EMBCPROD.TBL_REPORT  AS UCR 
         ON UCR.REPORT_ID = REPT.CREATED_BY 
         LEFT JOIN EMBCPROD.TBL_USER  AS UC 
         ON UCR.GLOBAL_REPORT_ID = UC.USER_ID AND UCR.STATUS IN ( 'A', 'D' ) 
         LEFT JOIN EMBCPROD.TBL_REPORT  AS UMR 
         ON UMR.REPORT_ID = REPT.MODIFIED_BY 
         LEFT JOIN EMBCPROD.TBL_USER  AS UM 
         ON UM.USER_ID = UMR.GLOBAL_REPORT_ID AND UMR.STATUS IN ( 'A', 'D' )
   WHERE TSA.TIP_INTEL_TSA_ID = REPT.GLOBAL_REPORT_ID
GO
EXECUTE sp_addextendedproperty @name = N'MS_SSMA_SOURCE', @value = N'EMBCPROD.V_TIP_INTEL_TSA_REPORT', @level0type = N'SCHEMA', @level0name = N'EMBCPROD', @level1type = N'VIEW', @level1name = N'V_TIP_INTEL_TSA_REPORT';

