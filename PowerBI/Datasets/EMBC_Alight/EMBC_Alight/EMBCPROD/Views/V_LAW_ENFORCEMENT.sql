CREATE VIEW EMBCPROD.V_LAW_ENFORCEMENT (
   PARENT_KEY, 
   UNIQUE_KEY, 
   HISTORY_VERSION, 
   DELETED, 
   FOREIGN_DOC, 
   OWNED_BY, 
   FORM_TYPE, 
   PEOPLE_INVOLVED, 
   GROUP_AFFILIATION, 
   ACTIVITY_ENGAGED, 
   HOW_EQUIPPED, 
   DETAILS, 
   CREATION_DATE, 
   CREATED_BY, 
   MODIFICATION_DATE, 
   MODIFIED_BY, 
   TIMEZONE)
AS 
   SELECT 
      REPT.REPORT_ID AS PARENT_KEY, 
      LW.LAW_ENFORCEMENT_ID AS UNIQUE_KEY, 
      CASE LW.STATUS
         WHEN 'O' THEN 'Yes'
         ELSE 'No'
      END AS HISTORY_VERSION, 
      CASE REPT.STATUS
         WHEN 'D' THEN 'Yes'
         ELSE 'No'
      END AS DELETED, 
      CASE LW.CREATED_BY
         WHEN NULL THEN 'Yes'
         ELSE 'No'
      END AS FOREIGN_DOC, 
      CASE REPT.IS_OWNER
         WHEN 'Y' THEN 'Local'
         ELSE 'No'
      END AS OWNED_BY, 
      'Law Enforcement' AS FORM_TYPE, 
      LW.PEOPLE_INVOLVED, 
      LW.GROUP_AFFILIATION, 
      LW.ACTIVITY_ENGAGED, 
      LW.HOW_EQUIPPED, 
      LW.DETAILS, 
      LW.CREATION_DATE, 
      CASE 
         WHEN UC.USER_NAME IS NULL THEN 
            (
               SELECT TBL_REMOTE_DOCUMENT_USER_INFO.CREATED_BY_USER_LOGIN_ID
               FROM EMBCPROD.TBL_REMOTE_DOCUMENT_USER_INFO
               WHERE TBL_REMOTE_DOCUMENT_USER_INFO.GLOBAL_REPORT_ID = LW.GLOBAL_LAW_ENFORCEMENT_ID
            )
         ELSE UC.USER_NAME
      END AS CREATED_BY, 
      LW.MODIFICATION_DATE, 
      CASE 
         WHEN UM.USER_NAME IS NULL THEN 
            (
               SELECT TBL_REMOTE_DOCUMENT_USER_INFO$2.MODIFIED_BY_USER_LOGIN_ID
               FROM EMBCPROD.TBL_REMOTE_DOCUMENT_USER_INFO  AS TBL_REMOTE_DOCUMENT_USER_INFO$2
               WHERE TBL_REMOTE_DOCUMENT_USER_INFO$2.GLOBAL_REPORT_ID = LW.GLOBAL_LAW_ENFORCEMENT_ID
            )
         ELSE UM.USER_NAME
      END AS MODIFIED_BY, 
      
         (
            SELECT TBL_EEMS_CONFIGURATION.CUSTOMER_TIMEZONE
            FROM EMBCPROD.TBL_EEMS_CONFIGURATION, EMBCPROD.TBL_REPORT
            WHERE TBL_EEMS_CONFIGURATION.CONFIGURATION_ID = TBL_REPORT.GLOBAL_REPORT_ID AND TBL_REPORT.STATUS = 'A'
         ) AS TIMEZONE
   FROM 
      EMBCPROD.TBL_REPORT  AS REPT, 
      EMBCPROD.TBL_INCIDENT  AS I 
         LEFT JOIN EMBCPROD.TBL_INCIDENT_LAW_ENFORCEMENT  AS LW 
         ON I.LAW_ENFORCEMENT_ID = LW.GLOBAL_LAW_ENFORCEMENT_ID 
         LEFT JOIN EMBCPROD.TBL_REPORT  AS UCR 
         ON UCR.REPORT_ID = LW.CREATED_BY 
         LEFT JOIN EMBCPROD.TBL_USER  AS UC 
         ON UCR.GLOBAL_REPORT_ID = UC.USER_ID AND UCR.STATUS IN ( 'A', 'D' ) 
         LEFT JOIN EMBCPROD.TBL_REPORT  AS UMR 
         ON UMR.REPORT_ID = LW.MODIFIED_BY 
         LEFT JOIN EMBCPROD.TBL_USER  AS UM 
         ON UM.USER_ID = UMR.GLOBAL_REPORT_ID AND UMR.STATUS IN ( 'A', 'D' )
   WHERE I.INCIDENT_ID = REPT.GLOBAL_REPORT_ID AND LW.LAW_ENFORCEMENT_ID IS NOT NULL
GO
EXECUTE sp_addextendedproperty @name = N'MS_SSMA_SOURCE', @value = N'EMBCPROD.V_LAW_ENFORCEMENT', @level0type = N'SCHEMA', @level0name = N'EMBCPROD', @level1type = N'VIEW', @level1name = N'V_LAW_ENFORCEMENT';

