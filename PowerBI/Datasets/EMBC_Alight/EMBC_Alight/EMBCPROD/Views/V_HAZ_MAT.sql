CREATE VIEW EMBCPROD.V_HAZ_MAT (
   PARENT_KEY, 
   UNIQUE_KEY, 
   HISTORY_VERSION, 
   DELETED, 
   FOREIGN_DOC, 
   OWNED_BY, 
   FORM_TYPE, 
   THREAT_TYPE, 
   THREAT_PROPERTIES, 
   MATERIAL_FORM, 
   AGENTS, 
   IMMEDIATE_HAZARD, 
   IMMEDIATE_HAZARD_COMMENT, 
   EVACUATION_RECOMM, 
   EVACUATION_RECOMM_COMMENT, 
   DECONTAMINATION_REQ, 
   DECONTAMINATION_REQ_COMMENT, 
   PROPOGATION, 
   DIRECTION_MOVING, 
   CREATION_DATE, 
   CREATED_BY, 
   MODIFICATION_DATE, 
   MODIFIED_BY, 
   TIMEZONE)
AS 
   SELECT 
      REPT.REPORT_ID AS PARENT_KEY, 
      H.HAZ_MAT_ID AS UNIQUE_KEY, 
      CASE H.STATUS
         WHEN 'O' THEN 'Yes'
         ELSE 'No'
      END AS HISTORY_VERSION, 
      CASE REPT.STATUS
         WHEN 'D' THEN 'Yes'
         ELSE 'No'
      END AS DELETED, 
      CASE H.CREATED_BY
         WHEN NULL THEN 'Yes'
         ELSE 'No'
      END AS FOREIGN_DOC, 
      CASE REPT.IS_OWNER
         WHEN 'Y' THEN 'Local'
         ELSE 'No'
      END AS OWNED_BY, 
      'Haz Mat' AS FORM_TYPE, 
      H.THREAT_TYPE, 
      H.THREAT_PROPERTIES, 
      H.MATERIAL_FORM, 
      H.AGENTS, 
      H.IMMEDIATE_HAZARD, 
      H.IMMEDIATE_HAZARD_COMMENT, 
      H.EVACUATION_RECOMM, 
      H.EVACUATION_RECOMM_COMMENT, 
      H.DECONTAMINATION_REQ, 
      H.DECONTAMINATION_REQ_COMMENT, 
      H.PROPOGATION, 
      H.DIRECTION_MOVING, 
      H.CREATION_DATE, 
      CASE 
         WHEN UC.USER_NAME IS NULL THEN 
            (
               SELECT TBL_REMOTE_DOCUMENT_USER_INFO.CREATED_BY_USER_LOGIN_ID
               FROM EMBCPROD.TBL_REMOTE_DOCUMENT_USER_INFO
               WHERE TBL_REMOTE_DOCUMENT_USER_INFO.GLOBAL_REPORT_ID = H.GLOBAL_HAZ_MAT_ID
            )
         ELSE UC.USER_NAME
      END AS CREATED_BY, 
      H.MODIFICATION_DATE, 
      CASE 
         WHEN UM.USER_NAME IS NULL THEN 
            (
               SELECT TBL_REMOTE_DOCUMENT_USER_INFO$2.MODIFIED_BY_USER_LOGIN_ID
               FROM EMBCPROD.TBL_REMOTE_DOCUMENT_USER_INFO  AS TBL_REMOTE_DOCUMENT_USER_INFO$2
               WHERE TBL_REMOTE_DOCUMENT_USER_INFO$2.GLOBAL_REPORT_ID = H.GLOBAL_HAZ_MAT_ID
            )
         ELSE UM.USER_NAME
      END AS MODIFIED_BY, 
      
         (
            SELECT TBL_EEMS_CONFIGURATION.CUSTOMER_TIMEZONE
            FROM EMBCPROD.TBL_EEMS_CONFIGURATION, EMBCPROD.TBL_REPORT
            WHERE TBL_EEMS_CONFIGURATION.CONFIGURATION_ID = TBL_REPORT.GLOBAL_REPORT_ID AND TBL_REPORT.STATUS = 'A'
         ) AS TIMEZONE
   FROM 
      EMBCPROD.TBL_REPORT  AS REPT, 
      EMBCPROD.TBL_INCIDENT  AS I 
         LEFT JOIN EMBCPROD.TBL_INCIDENT_HAZ_MAT  AS H 
         ON I.HAZ_MAT_ID = H.GLOBAL_HAZ_MAT_ID 
         LEFT JOIN EMBCPROD.TBL_REPORT  AS UCR 
         ON UCR.REPORT_ID = H.CREATED_BY 
         LEFT JOIN EMBCPROD.TBL_USER  AS UC 
         ON UCR.GLOBAL_REPORT_ID = UC.USER_ID AND UCR.STATUS IN ( 'A', 'D' ) 
         LEFT JOIN EMBCPROD.TBL_REPORT  AS UMR 
         ON UMR.REPORT_ID = H.MODIFIED_BY 
         LEFT JOIN EMBCPROD.TBL_USER  AS UM 
         ON UM.USER_ID = UMR.GLOBAL_REPORT_ID AND UMR.STATUS IN ( 'A', 'D' )
   WHERE I.INCIDENT_ID = REPT.GLOBAL_REPORT_ID AND H.HAZ_MAT_ID IS NOT NULL
GO
EXECUTE sp_addextendedproperty @name = N'MS_SSMA_SOURCE', @value = N'EMBCPROD.V_HAZ_MAT', @level0type = N'SCHEMA', @level0name = N'EMBCPROD', @level1type = N'VIEW', @level1name = N'V_HAZ_MAT';

