CREATE VIEW EMBCPROD.V_CASE_DEPENDENT (
   CASE_DEPENDENT_ID, 
   CASE_NUMBER, 
   CASE_STATUS, 
   CITIZENSHIP, 
   DATE_OF_BIRTH, 
   FIRST_NAME, 
   LAST_NAME, 
   EMPLOYER_NAME)
AS 
   SELECT 
      CD.CASE_DEPENDENT_ID, 
      CD.CASE_NUMBER, 
      CD.CASE_STATUS, 
      CD.CITIZENSHIP, 
      CD.DATE_OF_BIRTH, 
      CD.FIRST_NAME, 
      CD.LAST_NAME, 
      CD.EMPLOYER_NAME
   FROM EMBCPROD.TBL_CASE_DEPENDENT  AS CD
GO
EXECUTE sp_addextendedproperty @name = N'MS_SSMA_SOURCE', @value = N'EMBCPROD.V_CASE_DEPENDENT', @level0type = N'SCHEMA', @level0name = N'EMBCPROD', @level1type = N'VIEW', @level1name = N'V_CASE_DEPENDENT';

