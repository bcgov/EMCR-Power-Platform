CREATE VIEW EMBCPROD.V_ENTITY_INTEL_REPORT (
   ENTITY_INTEL_ID, 
   ENTITY_NAME, 
   REPORTING_TIME, 
   THREAT_LEVEL, 
   THREAT_ORIGIN)
AS 
   SELECT 
      EI.ENTITY_INTEL_ID, 
      EI.ENTITY_NAME, 
      EI.REPORTING_TIME, 
      EI.THREAT_LEVEL, 
      EI.THREAT_ORIGIN
   FROM EMBCPROD.TBL_ENTITY_INTEL_REPORT  AS EI
GO
EXECUTE sp_addextendedproperty @name = N'MS_SSMA_SOURCE', @value = N'EMBCPROD.V_ENTITY_INTEL_REPORT', @level0type = N'SCHEMA', @level0name = N'EMBCPROD', @level1type = N'VIEW', @level1name = N'V_ENTITY_INTEL_REPORT';

