CREATE VIEW EMBCPROD.V_PUBLIC_INFO_REPORT (
   PUBLIC_INFO_ID, 
   CURRENT_STATUS, 
   INFO_SUBJECT, 
   PARENT_REPORT_ID)
AS 
   SELECT PIO.PUBLIC_INFO_ID, PIO.CURRENT_STATUS, PIO.INFO_SUBJECT, PIO.PARENT_REPORT_ID
   FROM EMBCPROD.TBL_PUBLIC_INFO_REPORT  AS PIO
GO
EXECUTE sp_addextendedproperty @name = N'MS_SSMA_SOURCE', @value = N'EMBCPROD.V_PUBLIC_INFO_REPORT', @level0type = N'SCHEMA', @level0name = N'EMBCPROD', @level1type = N'VIEW', @level1name = N'V_PUBLIC_INFO_REPORT';

