CREATE VIEW EMBCPROD.V_PERSONNEL (
   UNIQUE_KEY, 
   HISTORY_VERSION, 
   DELETED, 
   FOREIGN_DOC, 
   WEB_PAGE_LINK, 
   DATASHARE_COMMENTS, 
   OWNED_BY, 
   PERSONAL_LAST_NAME, 
   PERSONAL_FIRST_NAME, 
   PERSONAL_MIDDLE_INITIAL, 
   PERSONAL_JOB_TITLE, 
   PERSONAL_CATEGORY, 
   ORGANIZATION_NAME, 
   ASSIGNED_FACILITY, 
   PERSONAL_SUPERVISOR, 
   PERSONAL_SPECIAL_NEEDS, 
   PERSONAL_SPECIAL_NEEDS_DET, 
   PERSONAL_WORK_PHONE, 
   PERSONAL_HOME_PHONE, 
   PERSONAL_CELL_PHONE, 
   PERSONAL_PAGER, 
   PERSONAL_EMAIL, 
   PERSONAL_MAIL_STOP, 
   EMERGENCY_LAST_NAME, 
   EMERGENCY_MIDDLE_INITIAL, 
   EMERGENCY_FIRST_NAME, 
   EMERGENCY_RELATIONSHIP, 
   EMERGENCY_WORK_PHONE, 
   EMERGENCY_HOME_PHONE, 
   EMERGENCY_CELL_PHONE, 
   EMERGENCY_PAGER, 
   EMERGENCY_EMAIL, 
   EMERGENCY_STATUS, 
   EMERGENCY_CURRENT_LOCATION, 
   RELATED_REPORT_NAME, 
   EMERGENCY_FAMILY_CONTACTED, 
   EMERGENCY_COMMENTS, 
   EMERGENCY_OTHER_COMMENTS, 
   SITE_NAME, 
   SITE_TYPE, 
   STREET_ADDRESS, 
   APT_LOT_NO, 
   CITY, 
   COUNTRY_NAME, 
   COUNTY_NAME, 
   GEO_AREA, 
   STATE_NAME, 
   ZIP_CODE, 
   INTERSECTION_STREET1, 
   INTERSECTION_STREET2, 
   ADDITIONAL_INFO, 
   SHOW_ON_MAP, 
   LATITUDE, 
   LONGITUDE, 
   F_LATITUDE, 
   F_LONGITUDE, 
   GEO_LOCATED_BY, 
   CREATION_DATE, 
   CREATED_BY, 
   MODIFICATION_DATE, 
   MODIFIED_BY, 
   TIMEZONE)
AS 
   SELECT 
      PSL.PERSONNEL_ID AS UNIQUE_KEY, 
      CASE REPT.STATUS
         WHEN 'O' THEN 'Yes'
         ELSE 'No'
      END AS HISTORY_VERSION, 
      CASE REPT.STATUS
         WHEN 'D' THEN 'Yes'
         ELSE 'No'
      END AS DELETED, 
      CASE REPT.CREATED_BY
         WHEN NULL THEN 'Yes'
         ELSE 'No'
      END AS FOREIGN_DOC, 
      REPT.WEB_PAGE_LINK, 
      DS.COMMENTS AS DATASHARE_COMMENTS, 
      CASE REPT.IS_OWNER
         WHEN 'Y' THEN 'Local'
         ELSE 'No'
      END AS OWNED_BY, 
      PSL.PERSONAL_LAST_NAME, 
      PSL.PERSONAL_FIRST_NAME, 
      PSL.PERSONAL_MIDDLE_INITIAL, 
      PSL.PERSONAL_JOB_TITLE, 
      PSL.PERSONAL_CATEGORY, 
      ORG.ORGANIZATION_NAME, 
      S.SITE_NAME AS ASSIGNED_FACILITY, 
      PSL.PERSONAL_SUPERVISOR, 
      PSL.PERSONAL_SPECIAL_NEEDS, 
      PSL.PERSONAL_SPECIAL_NEEDS_DET, 
      PSL.PERSONAL_WORK_PHONE, 
      PSL.PERSONAL_HOME_PHONE, 
      PSL.PERSONAL_CELL_PHONE, 
      PSL.PERSONAL_PAGER, 
      PSL.PERSONAL_EMAIL, 
      PSL.PERSONAL_MAIL_STOP, 
      PSL.EMERGENCY_LAST_NAME, 
      PSL.EMERGENCY_MIDDLE_INITIAL, 
      PSL.EMERGENCY_FIRST_NAME, 
      PSL.EMERGENCY_RELATIONSHIP, 
      PSL.EMERGENCY_WORK_PHONE, 
      PSL.EMERGENCY_HOME_PHONE, 
      PSL.EMERGENCY_CELL_PHONE, 
      PSL.EMERGENCY_PAGER, 
      PSL.EMERGENCY_EMAIL, 
      PSL.EMERGENCY_STATUS, 
      PSL.EMERGENCY_CURRENT_LOCATION, 
      RR.RELATED_REPORT_NAME, 
      PSL.EMERGENCY_FAMILY_CONTACTED, 
      PSL.EMERGENCY_COMMENTS, 
      PSL.EMERGENCY_OTHER_COMMENTS, 
      L.SITE_NAME, 
      L.SITE_TYPE, 
      L.STREET_ADDRESS, 
      L.APT_LOT_NO, 
      L.CITY, 
      L.COUNTRY_NAME, 
      L.COUNTY_NAME, 
      L.GEO_AREA, 
      L.STATE_NAME, 
      L.ZIP_CODE, 
      L.INTERSECTION_STREET1, 
      L.INTERSECTION_STREET2, 
      L.ADDITIONAL_INFO, 
      G.SHOW_ON_MAP, 
      G.LATITUDE, 
      G.LONGITUDE, 
      CAST(G.LATITUDE AS float(53)) AS F_LATITUDE, 
      CAST(G.LONGITUDE AS float(53)) AS F_LONGITUDE, 
      G.GEO_LOCATED_BY, 
      REPT.CREATION_DATE, 
      CASE 
         WHEN UC.USER_NAME IS NULL THEN 
            (
               SELECT TBL_REMOTE_DOCUMENT_USER_INFO.CREATED_BY_USER_LOGIN_ID
               FROM EMBCPROD.TBL_REMOTE_DOCUMENT_USER_INFO
               WHERE TBL_REMOTE_DOCUMENT_USER_INFO.GLOBAL_REPORT_ID = PSL.PERSONNEL_ID
            )
         ELSE UC.USER_NAME
      END AS CREATED_BY, 
      REPT.MODIFICATION_DATE, 
      CASE 
         WHEN UM.USER_NAME IS NULL THEN 
            (
               SELECT TBL_REMOTE_DOCUMENT_USER_INFO$2.MODIFIED_BY_USER_LOGIN_ID
               FROM EMBCPROD.TBL_REMOTE_DOCUMENT_USER_INFO  AS TBL_REMOTE_DOCUMENT_USER_INFO$2
               WHERE TBL_REMOTE_DOCUMENT_USER_INFO$2.GLOBAL_REPORT_ID = PSL.PERSONNEL_ID
            )
         ELSE UM.USER_NAME
      END AS MODIFIED_BY, 
      
         (
            SELECT TBL_EEMS_CONFIGURATION.CUSTOMER_TIMEZONE
            FROM EMBCPROD.TBL_EEMS_CONFIGURATION, EMBCPROD.TBL_REPORT
            WHERE TBL_EEMS_CONFIGURATION.CONFIGURATION_ID = TBL_REPORT.GLOBAL_REPORT_ID AND TBL_REPORT.STATUS = 'A'
         ) AS TIMEZONE
   FROM 
      ((EMBCPROD.TBL_PERSONNEL  AS PSL 
         LEFT JOIN EMBCPROD.TBL_ORGANIZATION  AS ORG 
         ON PSL.PERSONAL_ORGANIZATION_ID = ORG.ORGANIZATION_ID) 
         LEFT JOIN EMBCPROD.TBL_SITE  AS S 
         ON PSL.PERSONAL_ASSIGNED_FACILITY_ID = S.SITE_ID), 
      (((EMBCPROD.TBL_REPORT  AS REPT 
         LEFT JOIN EMBCPROD.TBL_LOCATION  AS L 
         ON REPT.LOCATION_ID = L.LOCATION_ID) 
         LEFT JOIN EMBCPROD.TBL_GEO_LOCATION_MAPPING  AS G 
         ON REPT.GEO_LOCATION_MAPPING_ID = G.GEO_LOCATION_MAPPING_ID) 
         LEFT JOIN EMBCPROD.TBL_DATA_SHARING  AS DS 
         ON REPT.DATA_SHARING_ID = DS.DATA_SHARING_ID) 
         LEFT JOIN EMBCPROD.TBL_RELATED_REPORT  AS RR 
         ON 
            REPT.GLOBAL_REPORT_ID = RR.GLOBAL_REPORT_ID AND 
            RR.RELATED_REPORT_TYPE = 'EIA' AND 
            RR.RELATED_GLOBAL_REPORT_ID IN 
            (
               SELECT TBL_REPORT$2.GLOBAL_REPORT_ID
               FROM EMBCPROD.TBL_REPORT  AS TBL_REPORT$2
               WHERE TBL_REPORT$2.STATUS IN ( 'A', 'C', 'D' )
            ) 
         LEFT JOIN EMBCPROD.TBL_REPORT  AS UCR 
         ON UCR.REPORT_ID = REPT.CREATED_BY 
         LEFT JOIN EMBCPROD.TBL_USER  AS UC 
         ON UCR.GLOBAL_REPORT_ID = UC.USER_ID AND UCR.STATUS IN ( 'A', 'D' ) 
         LEFT JOIN EMBCPROD.TBL_REPORT  AS UMR 
         ON UMR.REPORT_ID = REPT.MODIFIED_BY 
         LEFT JOIN EMBCPROD.TBL_USER  AS UM 
         ON UM.USER_ID = UMR.GLOBAL_REPORT_ID AND UMR.STATUS IN ( 'A', 'D' )
   WHERE PSL.PERSONNEL_ID = REPT.GLOBAL_REPORT_ID
GO
EXECUTE sp_addextendedproperty @name = N'MS_SSMA_SOURCE', @value = N'EMBCPROD.V_PERSONNEL', @level0type = N'SCHEMA', @level0name = N'EMBCPROD', @level1type = N'VIEW', @level1name = N'V_PERSONNEL';

