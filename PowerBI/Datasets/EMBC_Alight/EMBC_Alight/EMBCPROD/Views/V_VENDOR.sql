CREATE VIEW EMBCPROD.V_VENDOR (
   UNIQUE_KEY, 
   HISTORY_VERSION, 
   DELETED, 
   FOREIGN_DOC, 
   WEB_PAGE_LINK, 
   DATASHARE_COMMENTS, 
   OWNED_BY, 
   VENDOR_NAME, 
   VENDOR_STATUS, 
   VENDOR_COMMENTS, 
   VENDOR_IS_RELATIONSHIP, 
   VENDOR_CONTRACT_INFORMATION, 
   RELATED_REPORT_NAME, 
   VENDOR_POC_NAME_PRIMARY, 
   VENDOR_POC_NAME_SECONDARY, 
   VENDOR_POC_NAME_ALTERNATE, 
   VENDOR_POC_PHONE_PRIMARY, 
   VENDOR_POC_PHONE_SECONDARY, 
   VENDOR_POC_PHONE_ALTERNATE, 
   VENDOR_POC_FAX_PRIMARY, 
   VENDOR_POC_FAX_SECONDARY, 
   VENDOR_POC_FAX_ALTERNATE, 
   VENDOR_POC_PAGER_PRIMARY, 
   VENDOR_POC_PAGER_SECONDARY, 
   VENDOR_POC_PAGER_ALTERNATE, 
   VENDOR_POC_MOBILE_PRIMARY, 
   VENDOR_POC_MOBILE_SECONDARY, 
   VENDOR_POC_MOBILE_ALTERNATE, 
   VENDOR_POC_EMAIL_PRIMARY, 
   VENDOR_POC_EMAIL_SECONDARY, 
   VENDOR_POC_EMAIL_ALTERNATE, 
   VENDOR_WEBSITE_URL, 
   VENDOR_ORDER_PLACEMENT_PREF, 
   VENDOR_BASIC_COMMENTS, 
   VENDOR_RESOURCE_TYPE, 
   VENDOR_RESOURCE_TYPE_COMM, 
   SITE_NAME, 
   SITE_TYPE, 
   STREET_ADDRESS, 
   APT_LOT_NO, 
   CITY, 
   COUNTRY_NAME, 
   COUNTY_NAME, 
   GEO_AREA, 
   STATE_NAME, 
   ZIP_CODE, 
   INTERSECTION_STREET1, 
   INTERSECTION_STREET2, 
   ADDITIONAL_INFO, 
   SHOW_ON_MAP, 
   LATITUDE, 
   LONGITUDE, 
   F_LATITUDE, 
   F_LONGITUDE, 
   GEO_LOCATED_BY, 
   CREATION_DATE, 
   CREATED_BY, 
   MODIFICATION_DATE, 
   MODIFIED_BY, 
   TIMEZONE)
AS 
   /*
   *   SSMA warning messages:
   *   O2SS0273: Oracle SUBSTR function and SQL Server SUBSTRING function may give different results.
   */

   SELECT 
      VDR.VENDOR_ID AS UNIQUE_KEY, 
      CASE REPT.STATUS
         WHEN 'O' THEN 'Yes'
         ELSE 'No'
      END AS HISTORY_VERSION, 
      CASE REPT.STATUS
         WHEN 'D' THEN 'Yes'
         ELSE 'No'
      END AS DELETED, 
      CASE REPT.CREATED_BY
         WHEN NULL THEN 'Yes'
         ELSE 'No'
      END AS FOREIGN_DOC, 
      REPT.WEB_PAGE_LINK, 
      DS.COMMENTS AS DATASHARE_COMMENTS, 
      CASE REPT.IS_OWNER
         WHEN 'Y' THEN 'Local'
         ELSE 'No'
      END AS OWNED_BY, 
      VDR.VENDOR_NAME, 
      ISNULL(upper(substring(PV.COLOR_IMG_KEY, 7, (ssma_oracle.instr4_varchar(PV.COLOR_IMG_KEY, '.', 7, 1) - 7))), '') + ' - ' + ISNULL(PV.PICKLIST_VALUE, '') AS VENDOR_STATUS, 
      VDR.VENDOR_COMMENTS, 
      VDR.VENDOR_IS_RELATIONSHIP, 
      VDR.VENDOR_CONTRACT_INFORMATION, 
      RR.RELATED_REPORT_NAME, 
      VDR.VENDOR_POC_NAME_PRIMARY, 
      VDR.VENDOR_POC_NAME_SECONDARY, 
      VDR.VENDOR_POC_NAME_ALTERNATE, 
      VDR.VENDOR_POC_PHONE_PRIMARY, 
      VDR.VENDOR_POC_PHONE_SECONDARY, 
      VDR.VENDOR_POC_PHONE_ALTERNATE, 
      VDR.VENDOR_POC_FAX_PRIMARY, 
      VDR.VENDOR_POC_FAX_SECONDARY, 
      VDR.VENDOR_POC_FAX_ALTERNATE, 
      VDR.VENDOR_POC_PAGER_PRIMARY, 
      VDR.VENDOR_POC_PAGER_SECONDARY, 
      VDR.VENDOR_POC_PAGER_ALTERNATE, 
      VDR.VENDOR_POC_MOBILE_PRIMARY, 
      VDR.VENDOR_POC_MOBILE_SECONDARY, 
      VDR.VENDOR_POC_MOBILE_ALTERNATE, 
      VDR.VENDOR_POC_EMAIL_PRIMARY, 
      VDR.VENDOR_POC_EMAIL_SECONDARY, 
      VDR.VENDOR_POC_EMAIL_ALTERNATE, 
      VDR.VENDOR_WEBSITE_URL, 
      VDR.VENDOR_ORDER_PLACEMENT_PREF, 
      VDR.VENDOR_BASIC_COMMENTS, 
      VDR.VENDOR_RESOURCE_TYPE, 
      VDR.VENDOR_RESOURCE_TYPE_COMM, 
      L.SITE_NAME, 
      L.SITE_TYPE, 
      L.STREET_ADDRESS, 
      L.APT_LOT_NO, 
      L.CITY, 
      L.COUNTRY_NAME, 
      L.COUNTY_NAME, 
      L.GEO_AREA, 
      L.STATE_NAME, 
      L.ZIP_CODE, 
      L.INTERSECTION_STREET1, 
      L.INTERSECTION_STREET2, 
      L.ADDITIONAL_INFO, 
      G.SHOW_ON_MAP, 
      G.LATITUDE, 
      G.LONGITUDE, 
      CAST(G.LATITUDE AS float(53)) AS F_LATITUDE, 
      CAST(G.LONGITUDE AS float(53)) AS F_LONGITUDE, 
      G.GEO_LOCATED_BY, 
      REPT.CREATION_DATE, 
      CASE 
         WHEN UC.USER_NAME IS NULL THEN 
            (
               SELECT TBL_REMOTE_DOCUMENT_USER_INFO.CREATED_BY_USER_LOGIN_ID
               FROM EMBCPROD.TBL_REMOTE_DOCUMENT_USER_INFO
               WHERE TBL_REMOTE_DOCUMENT_USER_INFO.GLOBAL_REPORT_ID = VDR.VENDOR_ID
            )
         ELSE UC.USER_NAME
      END AS CREATED_BY, 
      REPT.MODIFICATION_DATE, 
      CASE 
         WHEN UM.USER_NAME IS NULL THEN 
            (
               SELECT TBL_REMOTE_DOCUMENT_USER_INFO$2.MODIFIED_BY_USER_LOGIN_ID
               FROM EMBCPROD.TBL_REMOTE_DOCUMENT_USER_INFO  AS TBL_REMOTE_DOCUMENT_USER_INFO$2
               WHERE TBL_REMOTE_DOCUMENT_USER_INFO$2.GLOBAL_REPORT_ID = VDR.VENDOR_ID
            )
         ELSE UM.USER_NAME
      END AS MODIFIED_BY, 
      
         (
            SELECT TBL_EEMS_CONFIGURATION.CUSTOMER_TIMEZONE
            FROM EMBCPROD.TBL_EEMS_CONFIGURATION, EMBCPROD.TBL_REPORT
            WHERE TBL_EEMS_CONFIGURATION.CONFIGURATION_ID = TBL_REPORT.GLOBAL_REPORT_ID AND TBL_REPORT.STATUS = 'A'
         ) AS TIMEZONE
   FROM 
      (EMBCPROD.TBL_VENDOR  AS VDR 
         LEFT JOIN EMBCPROD.TBL_PICKLIST_VALUE  AS PV 
         ON VDR.VENDOR_STATUS = PV.PICKLIST_VALUE_ID), 
      (((EMBCPROD.TBL_REPORT  AS REPT 
         LEFT JOIN EMBCPROD.TBL_LOCATION  AS L 
         ON REPT.LOCATION_ID = L.LOCATION_ID) 
         LEFT JOIN EMBCPROD.TBL_GEO_LOCATION_MAPPING  AS G 
         ON REPT.GEO_LOCATION_MAPPING_ID = G.GEO_LOCATION_MAPPING_ID) 
         LEFT JOIN EMBCPROD.TBL_DATA_SHARING  AS DS 
         ON REPT.DATA_SHARING_ID = DS.DATA_SHARING_ID) 
         LEFT JOIN EMBCPROD.TBL_RELATED_REPORT  AS RR 
         ON 
            REPT.GLOBAL_REPORT_ID = RR.GLOBAL_REPORT_ID AND 
            RR.RELATED_REPORT_TYPE = 'EIA' AND 
            RR.RELATED_GLOBAL_REPORT_ID IN 
            (
               SELECT TBL_REPORT$2.GLOBAL_REPORT_ID
               FROM EMBCPROD.TBL_REPORT  AS TBL_REPORT$2
               WHERE TBL_REPORT$2.STATUS IN ( 'A', 'C', 'D' )
            ) 
         LEFT JOIN EMBCPROD.TBL_REPORT  AS UCR 
         ON UCR.REPORT_ID = REPT.CREATED_BY 
         LEFT JOIN EMBCPROD.TBL_USER  AS UC 
         ON UCR.GLOBAL_REPORT_ID = UC.USER_ID AND UCR.STATUS IN ( 'A', 'D' ) 
         LEFT JOIN EMBCPROD.TBL_REPORT  AS UMR 
         ON UMR.REPORT_ID = REPT.MODIFIED_BY 
         LEFT JOIN EMBCPROD.TBL_USER  AS UM 
         ON UM.USER_ID = UMR.GLOBAL_REPORT_ID AND UMR.STATUS IN ( 'A', 'D' )
   WHERE VDR.VENDOR_ID = REPT.GLOBAL_REPORT_ID
GO
EXECUTE sp_addextendedproperty @name = N'MS_SSMA_SOURCE', @value = N'EMBCPROD.V_VENDOR', @level0type = N'SCHEMA', @level0name = N'EMBCPROD', @level1type = N'VIEW', @level1name = N'V_VENDOR';

