CREATE VIEW EMBCPROD.V_LOCATION_INTEL_REP (
   LOCATION_INTEL_ID, 
   REPORTING_TIME, 
   THREAT_LEVEL, 
   AREA_OF_OPERATION)
AS 
   SELECT LI.LOCATION_INTEL_ID, LI.REPORTING_TIME, LI.THREAT_LEVEL, LI.AREA_OF_OPERATION
   FROM EMBCPROD.TBL_LOCATION_INTEL_REP  AS LI
GO
EXECUTE sp_addextendedproperty @name = N'MS_SSMA_SOURCE', @value = N'EMBCPROD.V_LOCATION_INTEL_REP', @level0type = N'SCHEMA', @level0name = N'EMBCPROD', @level1type = N'VIEW', @level1name = N'V_LOCATION_INTEL_REP';

