CREATE VIEW EMBCPROD.V_EVENT (
   UNIQUE_KEY, 
   HISTORY_VERSION, 
   DELETED, 
   FOREIGN_DOC, 
   WEB_PAGE_LINK, 
   DATASHARE_COMMENTS, 
   OWNED_BY, 
   STATUS, 
   PROGNOSIS, 
   REPORT_TYPE, 
   EVENT_TYPE, 
   EVENT_NAME, 
   SEVERITY, 
   START_DATE, 
   ACTUAL_START_DATE, 
   COMPLETION_DATE, 
   ACTUAL_COMPLETION_DATE, 
   EVENT_DESC, 
   ACTION_PLAN, 
   EVENT_LOCATION, 
   SITE_NAME, 
   SITE_TYPE, 
   STREET_ADDRESS, 
   CITY, 
   STATE_NAME, 
   ZIP_CODE, 
   COUNTY_NAME, 
   GEO_AREA, 
   INTERSECTION_STREET1, 
   INTERSECTION_STREET2, 
   SHOW_ON_MAP, 
   LATITUDE, 
   LONGITUDE, 
   F_LATITUDE, 
   F_LONGITUDE, 
   GEO_LOCATED_BY, 
   ADDITIONAL_INFO, 
   RELATED_REPORTS, 
   INDIVIDUAL, 
   ORGANIZATION, 
   POSITION, 
   AGENCY, 
   CREATION_DATE, 
   CREATED_BY, 
   MODIFICATION_DATE, 
   MODIFIED_BY, 
   TIMEZONE)
AS 
   /*
   *   SSMA warning messages:
   *   O2SS0273: Oracle SUBSTR function and SQL Server SUBSTRING function may give different results.
   *   O2SS0273: Oracle SUBSTR function and SQL Server SUBSTRING function may give different results.
   */

   SELECT 
      E.EVENT_ID AS UNIQUE_KEY, 
      CASE REPT.STATUS
         WHEN 'O' THEN 'Yes'
         ELSE 'No'
      END AS HISTORY_VERSION, 
      CASE REPT.STATUS
         WHEN 'D' THEN 'Yes'
         ELSE 'No'
      END AS DELETED, 
      CASE REPT.CREATED_BY
         WHEN NULL THEN 'Yes'
         ELSE 'No'
      END AS FOREIGN_DOC, 
      REPT.WEB_PAGE_LINK, 
      DS.COMMENTS AS DATASHARE_COMMENTS, 
      CASE REPT.IS_OWNER
         WHEN 'Y' THEN 'Local'
         ELSE 'No'
      END AS OWNED_BY, 
      ISNULL(upper(substring(PV.COLOR_IMG_KEY, 7, (ssma_oracle.instr4_varchar(PV.COLOR_IMG_KEY, '.', 7, 1) - 7))), '') + ' - ' + ISNULL(PV.PICKLIST_VALUE, '') AS STATUS, 
      ISNULL(upper(substring(PV1.COLOR_IMG_KEY, 7, (ssma_oracle.instr4_varchar(PV1.COLOR_IMG_KEY, '.', 7, 1) - 7))), '') + ' - ' + ISNULL(PV1.PICKLIST_VALUE, '') AS PROGNOSIS, 
      CASE REPT.REPORT_TYPE
         WHEN 'emergency_event' THEN 'Emergency'
         WHEN 'planned_event' THEN 'Planned'
      END AS REPORT_TYPE, 
      E.EVENT_TYPE, 
      E.EVENT_NAME, 
      E.SEVERITY, 
      E.START_DATE, 
      E.ACTUAL_START_DATE, 
      E.COMPLETION_DATE, 
      E.ACTUAL_COMPLETION_DATE, 
      E.EVENT_DESC, 
      RR.RELATED_REPORT_NAME AS ACTION_PLAN, 
      E.EVENT_LOCATION, 
      L.SITE_NAME, 
      L.SITE_TYPE, 
      L.STREET_ADDRESS, 
      L.CITY, 
      L.STATE_NAME, 
      L.ZIP_CODE, 
      L.COUNTY_NAME, 
      L.GEO_AREA, 
      L.INTERSECTION_STREET1, 
      L.INTERSECTION_STREET2, 
      G.SHOW_ON_MAP, 
      G.LATITUDE, 
      G.LONGITUDE, 
      CAST(G.LATITUDE AS float(53)) AS F_LATITUDE, 
      CAST(G.LONGITUDE AS float(53)) AS F_LONGITUDE, 
      G.GEO_LOCATED_BY, 
      L.ADDITIONAL_INFO, 
      EMBCPROD.GET_RELATED_REPORT_FOR_EVENT(REPT.GLOBAL_REPORT_ID) AS RELATED_REPORTS, 
      RE.INDIVIDUAL, 
      RE.ORGANIZATION, 
      RE.POSITION, 
      RE.AGENCY, 
      REPT.CREATION_DATE, 
      CASE 
         WHEN UC.USER_NAME IS NULL THEN 
            (
               SELECT TBL_REMOTE_DOCUMENT_USER_INFO.CREATED_BY_USER_LOGIN_ID
               FROM EMBCPROD.TBL_REMOTE_DOCUMENT_USER_INFO
               WHERE TBL_REMOTE_DOCUMENT_USER_INFO.GLOBAL_REPORT_ID = E.EVENT_ID
            )
         ELSE UC.USER_NAME
      END AS CREATED_BY, 
      REPT.MODIFICATION_DATE, 
      CASE 
         WHEN UM.USER_NAME IS NULL THEN 
            (
               SELECT TBL_REMOTE_DOCUMENT_USER_INFO$2.MODIFIED_BY_USER_LOGIN_ID
               FROM EMBCPROD.TBL_REMOTE_DOCUMENT_USER_INFO  AS TBL_REMOTE_DOCUMENT_USER_INFO$2
               WHERE TBL_REMOTE_DOCUMENT_USER_INFO$2.GLOBAL_REPORT_ID = E.EVENT_ID
            )
         ELSE UM.USER_NAME
      END AS MODIFIED_BY, 
      
         (
            SELECT TBL_EEMS_CONFIGURATION.CUSTOMER_TIMEZONE
            FROM EMBCPROD.TBL_EEMS_CONFIGURATION, EMBCPROD.TBL_REPORT
            WHERE TBL_EEMS_CONFIGURATION.CONFIGURATION_ID = TBL_REPORT.GLOBAL_REPORT_ID AND TBL_REPORT.STATUS = 'A'
         ) AS TIMEZONE
   FROM 
      (EMBCPROD.TBL_EVENT  AS E 
         LEFT JOIN EMBCPROD.TBL_PICKLIST_VALUE  AS PV 
         ON E.CURRENT_STATUS = PV.PICKLIST_VALUE_ID) 
         LEFT JOIN EMBCPROD.TBL_PICKLIST_VALUE  AS PV1 
         ON E.PROGNOSIS = PV1.PICKLIST_VALUE_ID, 
      ((((EMBCPROD.TBL_REPORT  AS REPT 
         LEFT JOIN EMBCPROD.TBL_LOCATION  AS L 
         ON REPT.LOCATION_ID = L.LOCATION_ID) 
         LEFT JOIN EMBCPROD.TBL_GEO_LOCATION_MAPPING  AS G 
         ON REPT.GEO_LOCATION_MAPPING_ID = G.GEO_LOCATION_MAPPING_ID) 
         LEFT JOIN EMBCPROD.TBL_RESPONSIBLE_ENTITY  AS RE 
         ON REPT.ENTITY_ID = RE.ENTITY_ID) 
         LEFT JOIN EMBCPROD.TBL_DATA_SHARING  AS DS 
         ON REPT.DATA_SHARING_ID = DS.DATA_SHARING_ID) 
         LEFT JOIN EMBCPROD.TBL_RELATED_REPORT  AS RR 
         ON REPT.GLOBAL_REPORT_ID = RR.GLOBAL_REPORT_ID AND RR.RELATED_REPORT_TYPE = 'TASKSUBTASK' 
         LEFT JOIN EMBCPROD.TBL_REPORT  AS UCR 
         ON UCR.REPORT_ID = REPT.CREATED_BY 
         LEFT JOIN EMBCPROD.TBL_USER  AS UC 
         ON UCR.GLOBAL_REPORT_ID = UC.USER_ID AND UCR.STATUS IN ( 'A', 'D' ) 
         LEFT JOIN EMBCPROD.TBL_REPORT  AS UMR 
         ON UMR.REPORT_ID = REPT.MODIFIED_BY 
         LEFT JOIN EMBCPROD.TBL_USER  AS UM 
         ON UM.USER_ID = UMR.GLOBAL_REPORT_ID AND UMR.STATUS IN ( 'A', 'D' )
   WHERE E.EVENT_ID = REPT.GLOBAL_REPORT_ID
GO
EXECUTE sp_addextendedproperty @name = N'MS_SSMA_SOURCE', @value = N'EMBCPROD.V_EVENT', @level0type = N'SCHEMA', @level0name = N'EMBCPROD', @level1type = N'VIEW', @level1name = N'V_EVENT';

