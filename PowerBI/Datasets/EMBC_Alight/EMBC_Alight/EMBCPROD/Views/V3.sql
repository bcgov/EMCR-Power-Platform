CREATE VIEW EMBCPROD.V3 (
   REPORT_TYPE, 
   GLOBAL_REPORT_ID, 
   REPORT_ID, 
   REPORT_NAME, 
   REPORT_STATUS, 
   RELATED_GLOBAL_REPORT_ID, 
   RELATED_REPORT_ID, 
   RELATED_REPORT_TYPE, 
   RELATED_REPORT_NAME, 
   RELATED_REPORT_STATUS)
AS 
   SELECT 
      V1.REPORT_TYPE, 
      V1.GLOBAL_REPORT_ID, 
      V1.REPORT_ID, 
      V1.REPORT_NAME, 
      V1.REPORT_STATUS, 
      V1.RELATED_GLOBAL_REPORT_ID, 
      V1.RELATED_REPORT_ID, 
      V1.RELATED_REPORT_TYPE, 
      V1.RELATED_REPORT_NAME, 
      V1.RELATED_REPORT_STATUS
   FROM EMBCPROD.V1
   WHERE V1.REPORT_ID NOT IN 
      (
         SELECT V2.REPORT_ID
         FROM EMBCPROD.V2
      )
GO
EXECUTE sp_addextendedproperty @name = N'MS_SSMA_SOURCE', @value = N'EMBCPROD.V3', @level0type = N'SCHEMA', @level0name = N'EMBCPROD', @level1type = N'VIEW', @level1name = N'V3';

