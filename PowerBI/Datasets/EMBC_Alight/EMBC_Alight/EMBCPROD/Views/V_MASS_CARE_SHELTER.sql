CREATE VIEW EMBCPROD.V_MASS_CARE_SHELTER (
   PARENT_KEY, 
   UNIQUE_KEY, 
   HISTORY_VERSION, 
   DELETED, 
   FOREIGN_DOC, 
   OWNED_BY, 
   FORM_TYPE, 
   SHELTERS_OPEN, 
   SHELTERS_OPEN_COMMENT, 
   PERSON_DISPLACED, 
   PERSON_DISPLACED_COMMENT, 
   PERSON_IN_SHELTER, 
   PERSON_IN_SHELTER_COMMENT, 
   FEEDING_SITES, 
   FEEDING_SITES_COMMENTS, 
   PERSON_NOT_SHELTERED, 
   PERSON_NOT_SHELTERED_COMMENT, 
   MOBILE_FEED_SITES, 
   MOBILE_FEED_SITES_COMMENT, 
   PERSON_FED_24HRS, 
   PERSON_FED_24HRS_COMMENT, 
   PERSON_FED_24HRS_PROJ, 
   PERSON_FED_24HRS_PROJ_COMMENT, 
   CREATION_DATE, 
   CREATED_BY, 
   MODIFICATION_DATE, 
   MODIFIED_BY, 
   TIMEZONE)
AS 
   SELECT 
      REPT.REPORT_ID AS PARENT_KEY, 
      MC.MASS_CARE_SHELTER_ID AS UNIQUE_KEY, 
      CASE MC.STATUS
         WHEN 'O' THEN 'Yes'
         ELSE 'No'
      END AS HISTORY_VERSION, 
      CASE REPT.STATUS
         WHEN 'D' THEN 'Yes'
         ELSE 'No'
      END AS DELETED, 
      CASE MC.CREATED_BY
         WHEN NULL THEN 'Yes'
         ELSE 'No'
      END AS FOREIGN_DOC, 
      CASE REPT.IS_OWNER
         WHEN 'Y' THEN 'Local'
         ELSE 'No'
      END AS OWNED_BY, 
      'Mass Care Shelter' AS FORM_TYPE, 
      MC.SHELTERS_OPEN, 
      MC.SHELTERS_OPEN_COMMENT, 
      MC.PERSON_DISPLACED, 
      MC.PERSON_DISPLACED_COMMENT, 
      MC.PERSON_IN_SHELTER, 
      MC.PERSON_IN_SHELTER_COMMENT, 
      MC.FEEDING_SITES, 
      MC.FEEDING_SITES_COMMENTS, 
      MC.PERSON_NOT_SHELTERED, 
      MC.PERSON_NOT_SHELTERED_COMMENT, 
      MC.MOBILE_FEED_SITES, 
      MC.MOBILE_FEED_SITES_COMMENT, 
      MC.PERSON_FED_24HRS, 
      MC.PERSON_FED_24HRS_COMMENT, 
      MC.PERSON_FED_24HRS_PROJ, 
      MC.PERSON_FED_24HRS_PROJ_COMMENT, 
      MC.CREATION_DATE, 
      CASE 
         WHEN UC.USER_NAME IS NULL THEN 
            (
               SELECT TBL_REMOTE_DOCUMENT_USER_INFO.CREATED_BY_USER_LOGIN_ID
               FROM EMBCPROD.TBL_REMOTE_DOCUMENT_USER_INFO
               WHERE TBL_REMOTE_DOCUMENT_USER_INFO.GLOBAL_REPORT_ID = MC.GLOBAL_MASS_CARE_SHELTER_ID
            )
         ELSE UC.USER_NAME
      END AS CREATED_BY, 
      MC.MODIFICATION_DATE, 
      CASE 
         WHEN UM.USER_NAME IS NULL THEN 
            (
               SELECT TBL_REMOTE_DOCUMENT_USER_INFO$2.MODIFIED_BY_USER_LOGIN_ID
               FROM EMBCPROD.TBL_REMOTE_DOCUMENT_USER_INFO  AS TBL_REMOTE_DOCUMENT_USER_INFO$2
               WHERE TBL_REMOTE_DOCUMENT_USER_INFO$2.GLOBAL_REPORT_ID = MC.GLOBAL_MASS_CARE_SHELTER_ID
            )
         ELSE UM.USER_NAME
      END AS MODIFIED_BY, 
      
         (
            SELECT TBL_EEMS_CONFIGURATION.CUSTOMER_TIMEZONE
            FROM EMBCPROD.TBL_EEMS_CONFIGURATION, EMBCPROD.TBL_REPORT
            WHERE TBL_EEMS_CONFIGURATION.CONFIGURATION_ID = TBL_REPORT.GLOBAL_REPORT_ID AND TBL_REPORT.STATUS = 'A'
         ) AS TIMEZONE
   FROM 
      EMBCPROD.TBL_REPORT  AS REPT, 
      EMBCPROD.TBL_INCIDENT  AS I 
         LEFT JOIN EMBCPROD.TBL_INCIDENT_MASS_CARE_SHELTER  AS MC 
         ON I.MASS_CARE_SHELTER_ID = MC.GLOBAL_MASS_CARE_SHELTER_ID 
         LEFT JOIN EMBCPROD.TBL_REPORT  AS UCR 
         ON UCR.REPORT_ID = MC.CREATED_BY 
         LEFT JOIN EMBCPROD.TBL_USER  AS UC 
         ON UCR.GLOBAL_REPORT_ID = UC.USER_ID AND UCR.STATUS IN ( 'A', 'D' ) 
         LEFT JOIN EMBCPROD.TBL_REPORT  AS UMR 
         ON UMR.REPORT_ID = MC.MODIFIED_BY 
         LEFT JOIN EMBCPROD.TBL_USER  AS UM 
         ON UM.USER_ID = UMR.GLOBAL_REPORT_ID AND UMR.STATUS IN ( 'A', 'D' )
   WHERE I.INCIDENT_ID = REPT.GLOBAL_REPORT_ID AND MC.MASS_CARE_SHELTER_ID IS NOT NULL
GO
EXECUTE sp_addextendedproperty @name = N'MS_SSMA_SOURCE', @value = N'EMBCPROD.V_MASS_CARE_SHELTER', @level0type = N'SCHEMA', @level0name = N'EMBCPROD', @level1type = N'VIEW', @level1name = N'V_MASS_CARE_SHELTER';

