CREATE TABLE [Eteam].[JMS_MESSAGES] (
    [MESSAGEID]       INT           NOT NULL,
    [DESTINATION]     VARCHAR (150) NOT NULL,
    [TXID]            INT           NULL,
    [TXOP]            CHAR (1)      NULL,
    [MESSAGEBLOB]     IMAGE         NULL,
    [DELIVERYERROR]   VARCHAR (255) NULL,
    [JMS_MESSAGES_PK] INT           IDENTITY (1, 1) NOT NULL
);


GO
CREATE NONCLUSTERED INDEX [JMS_MESSAGES_DESTINATION]
    ON [Eteam].[JMS_MESSAGES]([DESTINATION] ASC);


GO
CREATE NONCLUSTERED INDEX [JMS_MESSAGES_TXOP_TXID]
    ON [Eteam].[JMS_MESSAGES]([TXOP] ASC, [TXID] ASC);

