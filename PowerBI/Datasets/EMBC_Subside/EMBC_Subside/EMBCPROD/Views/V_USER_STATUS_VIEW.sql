
CREATE VIEW [EMBCPROD].[V_USER_STATUS_VIEW] (
   USER_NAME, 
   USER_ID, 
   ASSIGNED_TO, 
   STATUS, 
   USER_GROUP, 
   WEB_PAGE_LINK, 
   CREATION_DATE, 
   MODIFICATION_DATE)
AS 
   SELECT 
      UV.USER_NAME, 
      UV.USER_ID, 
      UV.ASSIGNED_TO, 
      REPT.STATUS, 
      EMBCPROD.GET_USER_GROUPS(UV.USER_ID) AS USER_GROUP, 
      REPT.WEB_PAGE_LINK, 
      REPT.CREATION_DATE, 
      REPT.MODIFICATION_DATE
   FROM 
      EMBCPROD.TBL_USER  AS UV 
         LEFT JOIN EMBCPROD.TBL_REPORT  AS REPT 
         ON UV.USER_ID = REPT.GLOBAL_REPORT_ID AND REPT.STATUS = 'A'
   WHERE UV.USER_ID = REPT.GLOBAL_REPORT_ID

GO
EXECUTE sp_addextendedproperty @name = N'MS_SSMA_SOURCE', @value = N'EMBCPROD.V_USER_STATUS_VIEW', @level0type = N'SCHEMA', @level0name = N'EMBCPROD', @level1type = N'VIEW', @level1name = N'V_USER_STATUS_VIEW';

