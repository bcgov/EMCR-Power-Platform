
CREATE VIEW [EMBCPROD].[V_CASUALTIES] (
   PARENT_KEY, 
   UNIQUE_KEY, 
   HISTORY_VERSION, 
   DELETED, 
   FOREIGN_DOC, 
   OWNED_BY, 
   FORM_TYPE, 
   CIVILIAN_FATALITIES, 
   RESPONDERS_FATALITIES, 
   CIVILIAN_INJURIES, 
   RESPONDERS_INJURIES, 
   CIVILIAN_MISSING, 
   RESPONDERS_MISSING, 
   TOTAL_CIVILIANS, 
   TOTAL_RESPONDERS, 
   COMMENTS, 
   CREATION_DATE, 
   CREATED_BY, 
   MODIFICATION_DATE, 
   MODIFIED_BY, 
   TIMEZONE)
AS 
   SELECT 
      REPT.REPORT_ID AS PARENT_KEY, 
      IC.CASUALTIES_ID AS UNIQUE_KEY, 
      CASE IC.STATUS
         WHEN 'O' THEN 'Yes'
         ELSE 'No'
      END AS HISTORY_VERSION, 
      CASE REPT.STATUS
         WHEN 'D' THEN 'Yes'
         ELSE 'No'
      END AS DELETED, 
      CASE IC.CREATED_BY
         WHEN NULL THEN 'Yes'
         ELSE 'No'
      END AS FOREIGN_DOC, 
      CASE REPT.IS_OWNER
         WHEN 'Y' THEN 'Local'
         ELSE 'No'
      END AS OWNED_BY, 
      'Casualties' AS FORM_TYPE, 
      IC.CIVILIAN_FATALITIES, 
      IC.RESPONDERS_FATALITIES, 
      IC.CIVILIAN_INJURIES, 
      IC.RESPONDERS_INJURIES, 
      IC.CIVILIAN_MISSING, 
      IC.RESPONDERS_MISSING, 
      CAST(replace(CAST(IC.CIVILIAN_FATALITIES AS varchar(max)), ',', NULL) AS numeric(38, 10)) + (CAST(replace(CAST(IC.CIVILIAN_INJURIES AS varchar(max)), ',', NULL) AS numeric(38, 10)) + CAST(replace(CAST(IC.CIVILIAN_MISSING AS varchar(max)), ',', NULL) AS numeric(38, 10))) AS TOTAL_CIVILIANS, 
      CAST(replace(CAST(IC.RESPONDERS_FATALITIES AS varchar(max)), ',', NULL) AS numeric(38, 10)) + CAST(replace(CAST(IC.RESPONDERS_INJURIES AS varchar(max)), ',', NULL) AS numeric(38, 10)) + CAST(replace(CAST(IC.RESPONDERS_MISSING AS varchar(max)), ',', NULL) AS numeric(38, 10)) AS TOTAL_RESPONDERS, 
      IC.COMMENTS, 
      IC.CREATION_DATE, 
      CASE 
         WHEN UC.USER_NAME IS NULL THEN 
            (
               SELECT TBL_REMOTE_DOCUMENT_USER_INFO.CREATED_BY_USER_LOGIN_ID
               FROM EMBCPROD.TBL_REMOTE_DOCUMENT_USER_INFO
               WHERE TBL_REMOTE_DOCUMENT_USER_INFO.GLOBAL_REPORT_ID = IC.GLOBAL_CASUALTIES_ID
            )
         ELSE UC.USER_NAME
      END AS CREATED_BY, 
      IC.MODIFICATION_DATE, 
      CASE 
         WHEN UM.USER_NAME IS NULL THEN 
            (
               SELECT TBL_REMOTE_DOCUMENT_USER_INFO$2.MODIFIED_BY_USER_LOGIN_ID
               FROM EMBCPROD.TBL_REMOTE_DOCUMENT_USER_INFO  AS TBL_REMOTE_DOCUMENT_USER_INFO$2
               WHERE TBL_REMOTE_DOCUMENT_USER_INFO$2.GLOBAL_REPORT_ID = IC.GLOBAL_CASUALTIES_ID
            )
         ELSE UM.USER_NAME
      END AS MODIFIED_BY, 
      
         (
            SELECT TBL_EEMS_CONFIGURATION.CUSTOMER_TIMEZONE
            FROM EMBCPROD.TBL_EEMS_CONFIGURATION, EMBCPROD.TBL_REPORT
            WHERE TBL_EEMS_CONFIGURATION.CONFIGURATION_ID = TBL_REPORT.GLOBAL_REPORT_ID AND TBL_REPORT.STATUS = 'A'
         ) AS TIMEZONE
   FROM 
      EMBCPROD.TBL_REPORT  AS REPT, 
      EMBCPROD.TBL_INCIDENT  AS I 
         LEFT JOIN EMBCPROD.TBL_INCIDENT_CASUALTIES  AS IC 
         ON I.CASUALTIES_ID = IC.GLOBAL_CASUALTIES_ID 
         LEFT JOIN EMBCPROD.TBL_REPORT  AS UCR 
         ON UCR.REPORT_ID = IC.CREATED_BY 
         LEFT JOIN EMBCPROD.TBL_USER  AS UC 
         ON UCR.GLOBAL_REPORT_ID = UC.USER_ID AND UCR.STATUS IN ( 'A', 'D' ) 
         LEFT JOIN EMBCPROD.TBL_REPORT  AS UMR 
         ON UMR.REPORT_ID = IC.MODIFIED_BY 
         LEFT JOIN EMBCPROD.TBL_USER  AS UM 
         ON UM.USER_ID = UMR.GLOBAL_REPORT_ID AND UMR.STATUS IN ( 'A', 'D' )
   WHERE I.INCIDENT_ID = REPT.GLOBAL_REPORT_ID AND IC.CASUALTIES_ID IS NOT NULL

GO
EXECUTE sp_addextendedproperty @name = N'MS_SSMA_SOURCE', @value = N'EMBCPROD.V_CASUALTIES', @level0type = N'SCHEMA', @level0name = N'EMBCPROD', @level1type = N'VIEW', @level1name = N'V_CASUALTIES';

