
CREATE VIEW [EMBCPROD].[V_TIP_INTEL_REPORT] (
   UNIQUE_KEY, 
   HISTORY_VERSION, 
   DELETED, 
   FOREIGN_DOC, 
   WEB_PAGE_LINK, 
   DATASHARE_COMMENTS, 
   OWNED_BY, 
   TIP_INTEL_NUMBER, 
   TIP_INTEL_AGENCY, 
   TIP_INTEL_GISACNUMBER, 
   TIP_INTEL_GBINUMBER, 
   TIP_INTEL_FBINUMBER, 
   TIP_INTEL_DISPOSITION, 
   TIP_INTEL_DATETIME, 
   TIP_INTEL_LEADAGENCY, 
   TIP_INTEL_CASENUMBER, 
   TIP_INTEL_CONTACTNAME, 
   TIP_INTEL_TITLE, 
   TIP_INTEL_WORKPHONE, 
   TIP_INTEL_CELLPHONE, 
   TIP_INTEL_FAX, 
   TIP_INTEL_EMAIL, 
   TIP_INTEL_CONTACTTIME, 
   TIP_INTEL_SYNOPSIS, 
   TIP_INTEL_SYNOPSISVIEW, 
   TIP_INTEL_ACTIVE, 
   TIP_INTEL_PURPOSE, 
   TIP_INTEL_CASEAGENCY, 
   TIP_INTEL_STATUS, 
   RELATED_REPORT_NAME, 
   TIP_INTEL_RELATEDTIP, 
   SITE_NAME, 
   SITE_TYPE, 
   STREET_ADDRESS, 
   APT_LOT_NO, 
   CITY, 
   COUNTRY_NAME, 
   COUNTY_NAME, 
   GEO_AREA, 
   STATE_NAME, 
   ZIP_CODE, 
   INTERSECTION_STREET1, 
   INTERSECTION_STREET2, 
   ADDITIONAL_INFO, 
   SHOW_ON_MAP, 
   LATITUDE, 
   LONGITUDE, 
   F_LATITUDE, 
   F_LONGITUDE, 
   GEO_LOCATED_BY, 
   CREATION_DATE, 
   CREATED_BY, 
   MODIFICATION_DATE, 
   MODIFIED_BY, 
   TIMEZONE)
AS 
   SELECT 
      TR.TIP_INTEL_REPORT_ID AS UNIQUE_KEY, 
      CASE REPT.STATUS
         WHEN 'O' THEN 'Yes'
         ELSE 'No'
      END AS HISTORY_VERSION, 
      CASE REPT.STATUS
         WHEN 'D' THEN 'Yes'
         ELSE 'No'
      END AS DELETED, 
      CASE REPT.CREATED_BY
         WHEN NULL THEN 'Yes'
         ELSE 'No'
      END AS FOREIGN_DOC, 
      REPT.WEB_PAGE_LINK, 
      DS.COMMENTS AS DATASHARE_COMMENTS, 
      CASE REPT.IS_OWNER
         WHEN 'Y' THEN 'Local'
         ELSE 'No'
      END AS OWNED_BY, 
      TR.TIP_INTEL_NUMBER, 
      TR.TIP_INTEL_AGENCY, 
      TR.TIP_INTEL_GISACNUMBER, 
      TR.TIP_INTEL_GBINUMBER, 
      TR.TIP_INTEL_FBINUMBER, 
      TR.TIP_INTEL_DISPOSITION, 
      TR.TIP_INTEL_DATETIME, 
      TR.TIP_INTEL_LEADAGENCY, 
      TR.TIP_INTEL_CASENUMBER, 
      TR.TIP_INTEL_CONTACTNAME, 
      TR.TIP_INTEL_TITLE, 
      TR.TIP_INTEL_WORKPHONE, 
      TR.TIP_INTEL_CELLPHONE, 
      TR.TIP_INTEL_FAX, 
      TR.TIP_INTEL_EMAIL, 
      TR.TIP_INTEL_CONTACTTIME, 
      TR.TIP_INTEL_SYNOPSIS, 
      TR.TIP_INTEL_SYNOPSISVIEW, 
      TR.TIP_INTEL_ACTIVE, 
      TR.TIP_INTEL_PURPOSE, 
      TR.TIP_INTEL_CASEAGENCY, 
      PV.PICKLIST_VALUE AS TIP_INTEL_STATUS, 
      RR.RELATED_REPORT_NAME, 
      TR.TIP_INTEL_RELATEDTIP, 
      L.SITE_NAME, 
      L.SITE_TYPE, 
      L.STREET_ADDRESS, 
      L.APT_LOT_NO, 
      L.CITY, 
      L.COUNTRY_NAME, 
      L.COUNTY_NAME, 
      L.GEO_AREA, 
      L.STATE_NAME, 
      L.ZIP_CODE, 
      L.INTERSECTION_STREET1, 
      L.INTERSECTION_STREET2, 
      L.ADDITIONAL_INFO, 
      G.SHOW_ON_MAP, 
      G.LATITUDE, 
      G.LONGITUDE, 
      CAST(G.LATITUDE AS float(53)) AS F_LATITUDE, 
      CAST(G.LONGITUDE AS float(53)) AS F_LONGITUDE, 
      G.GEO_LOCATED_BY, 
      REPT.CREATION_DATE, 
      CASE 
         WHEN UC.USER_NAME IS NULL THEN 
            (
               SELECT TBL_REMOTE_DOCUMENT_USER_INFO.CREATED_BY_USER_LOGIN_ID
               FROM EMBCPROD.TBL_REMOTE_DOCUMENT_USER_INFO
               WHERE TBL_REMOTE_DOCUMENT_USER_INFO.GLOBAL_REPORT_ID = TR.TIP_INTEL_REPORT_ID
            )
         ELSE UC.USER_NAME
      END AS CREATED_BY, 
      REPT.MODIFICATION_DATE, 
      CASE 
         WHEN UM.USER_NAME IS NULL THEN 
            (
               SELECT TBL_REMOTE_DOCUMENT_USER_INFO$2.MODIFIED_BY_USER_LOGIN_ID
               FROM EMBCPROD.TBL_REMOTE_DOCUMENT_USER_INFO  AS TBL_REMOTE_DOCUMENT_USER_INFO$2
               WHERE TBL_REMOTE_DOCUMENT_USER_INFO$2.GLOBAL_REPORT_ID = TR.TIP_INTEL_REPORT_ID
            )
         ELSE UM.USER_NAME
      END AS MODIFIED_BY, 
      
         (
            SELECT TBL_EEMS_CONFIGURATION.CUSTOMER_TIMEZONE
            FROM EMBCPROD.TBL_EEMS_CONFIGURATION, EMBCPROD.TBL_REPORT
            WHERE TBL_EEMS_CONFIGURATION.CONFIGURATION_ID = TBL_REPORT.GLOBAL_REPORT_ID AND TBL_REPORT.STATUS = 'A'
         ) AS TIMEZONE
   FROM 
      EMBCPROD.TBL_TIP_INTEL_REPORT  AS TR 
         LEFT JOIN EMBCPROD.TBL_PICKLIST_VALUE  AS PV 
         ON TR.TIP_INTEL_STATUS = PV.PICKLIST_VALUE_ID, 
      (((EMBCPROD.TBL_REPORT  AS REPT 
         LEFT JOIN EMBCPROD.TBL_LOCATION  AS L 
         ON REPT.LOCATION_ID = L.LOCATION_ID) 
         LEFT JOIN EMBCPROD.TBL_GEO_LOCATION_MAPPING  AS G 
         ON REPT.GEO_LOCATION_MAPPING_ID = G.GEO_LOCATION_MAPPING_ID) 
         LEFT JOIN EMBCPROD.TBL_DATA_SHARING  AS DS 
         ON REPT.DATA_SHARING_ID = DS.DATA_SHARING_ID) 
         LEFT JOIN EMBCPROD.TBL_RELATED_REPORT  AS RR 
         ON RR.GLOBAL_REPORT_ID = REPT.GLOBAL_REPORT_ID 
         LEFT JOIN EMBCPROD.TBL_REPORT  AS UCR 
         ON UCR.REPORT_ID = REPT.CREATED_BY 
         LEFT JOIN EMBCPROD.TBL_USER  AS UC 
         ON UCR.GLOBAL_REPORT_ID = UC.USER_ID AND UCR.STATUS IN ( 'A', 'D' ) 
         LEFT JOIN EMBCPROD.TBL_REPORT  AS UMR 
         ON UMR.REPORT_ID = REPT.MODIFIED_BY 
         LEFT JOIN EMBCPROD.TBL_USER  AS UM 
         ON UM.USER_ID = UMR.GLOBAL_REPORT_ID AND UMR.STATUS IN ( 'A', 'D' )
   WHERE TR.TIP_INTEL_REPORT_ID = REPT.GLOBAL_REPORT_ID

GO
EXECUTE sp_addextendedproperty @name = N'MS_SSMA_SOURCE', @value = N'EMBCPROD.V_TIP_INTEL_REPORT', @level0type = N'SCHEMA', @level0name = N'EMBCPROD', @level1type = N'VIEW', @level1name = N'V_TIP_INTEL_REPORT';

